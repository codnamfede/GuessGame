    private void answernumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_answernumberActionPerformed
        try {
            int nro = Integer.parseInt(write_number.getText());
            playerguessconsole.append("You've writed: " + nro + "\n");
            playerguessconsole.append(playerguess.Guessnumber(nro));
            if (nro == playerguess.getnumber()) {
                playerguessconsole.append("-------\nClick to 'Start' if you wanna play again...\n-------\n");
                write_number.setText("");
                write_number.setEnabled(false);
                answernumber.setEnabled(false);
                startplayer.setText("Start");
            }
        } catch (NumberFormatException e) {
            playerguessconsole.append("You must write a number\n");
        }

    }//GEN-LAST:event_answernumberActionPerformed

